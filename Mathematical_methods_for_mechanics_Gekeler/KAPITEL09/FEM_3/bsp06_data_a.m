function DATA = bsp06_data_a(p)
% Rechte Seite fuer Exakte Loesung in Beispiel08

LP = size(p,2); DATA = zeros(6,LP); FAC = 10;
for I = 1:size(p,2)
   x = p(1,I); y = p(2,I);
   x2 = x*x; x3 = x2*x; x4 = x3*x; x5 = x4*x; x6 = x5*x; x7 = x6*x;
   y2 = y*y; y3 = y2*y; y4 = y3*y; y5 = y4*y; y6 = y5*y; y7 = y6*y;

   z    = - 8*(x-x2)^2*(y - y2)^2;
   w    =   16*((6*x2-6*x+1)*(y-y2)^2 + (x-x2)^2*(6*y2-6*y+1));
   u    = - 16*(x - x2)^2*(y - y2)*(1 - 2*y);
   v    =   16*(y - y2)^2*(x - x2)*(1 - 2*x);

deltaw = ...
64-384*x-384*y+576*x^2+576*y^2-2304*x*y^2+2304*x*y+2304*x^2*y^2 ...
-2304*x^2*y-384*y^3+192*y^4-384*x^3+192*x^4;

convecw = ...
-512*x^3*y+3072*x^3*y^2+2560*x^4*y-15360*x^4*y^2-3072*x^2*y^3 ...
+28160*x^4*y^3+512*x*y^3-2560*x*y^4-28160*x^3*y^4+61440*x^3*y^5 ...
+15360*x^2*y^4-27648*x^2*y^5-61440*x^5*y^3+27648*x^5*y^2-4608*x^5*y ...
-50176*x^3*y^6+21504*x^2*y^6+53760*x^5*y^4+14336*x^3*y^7-6144*x^2*y^7 ...
-53760*x^4*y^5+50176*x^6*y^3-21504*x^6*y^2+3584*x^6*y+53760*x^4*y^6 ...
-53760*x^6*y^4-15360*x^4*y^7+21504*x^6*y^5-14336*x^7*y^3+6144*x^7*y^2 ...
-1024*x^7*y-21504*x^5*y^6+15360*x^7*y^4+6144*x^5*y^7-6144*x^7*y^5 ...
+4608*x*y^5-3584*x*y^6+1024*x*y^7;

AUX = [FAC*[z;w;u;v;deltaw];FAC*FAC*convecw];

DATA(:,I) = AUX;
end

